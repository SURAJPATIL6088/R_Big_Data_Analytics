# Operators in R

# 1. Arithmatic
# 2. Relational
# 3. Logical
# 4. Assignment

a <- 10
b <- 5

# 1. Arithmatic
print(a+b)  # add
print(a-b)  # sub
print(a*b)  # mul
print(a/b)  # div
print(a%%b)  # Remainder
print(a%/%b)  # Quotient
print(a^b)  # power

# 2. Relational

print(a<b)  
print(a>b)  
print(a==b) 
print(a<=b) 
print(a>=b) 
print(a!=b)


# 3. Logical
# &
# |
# &&
# ||
# !


# 4. Assignment
# =
# <-
# ->
# <<-
# ->>
